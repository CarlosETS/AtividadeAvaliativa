package modelo;

/**
 *
 * @author IFPR
 */
public class Servidor extends Pessoa{

   private int matricula;
   private Carro carro;
   private String nome, cpf, tipodeServidor;
  

    /**
     * @return the matricula
     */
    public int getMatricula() {
        return matricula;
    }

    /**
     * @param matricula the matricula to set
     */
    public void setMatricula(int matricula) {
        this.matricula = matricula;
    }

    /**
     * @return the carro
     */
    public Carro getCarro() {
        return carro;
    }

    /**
     * @param carro the carro to set
     */
    public void setCarro(Carro carro) {
        this.carro = carro;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the cpf
     */
    public String getCpf() {
        return cpf;
    }

    /**
     * @param cpf the cpf to set
     */
    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    /**
     * @return the tipodeServidor
     */
    public String getTipodeServidor() {
        return tipodeServidor;
    }

    /**
     * @param tipodeServidor the tipodeServidor to set
     */
    public void setTipodeServidor(String tipodeServidor) {
        this.tipodeServidor = tipodeServidor;
    }

}
